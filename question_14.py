rocks = []
depth = len(input_txt.split('\n'))

for i, line in enumerate(input_txt.split('\n')):
	rows = len(line)
	for j, char in enumerate(line):
		if char == 'O':
			rocks.append((j,i))
		elif char == '#':
			rip.append((j,i))


def tilt_north(rocks):
	final_rock_pos = {}
	for i in range(rows):
		rip_in_row = [rock for rock in rip if rock[0]==i]
		rip_in_row.append((i,-1))
		rock_in_row = [rock for rock in rocks if rock[0]==i]

		rip_in_row = sorted(rip_in_row)[::-1]
		rock_in_row = sorted(rock_in_row)

		for rock in rock_in_row:
			for stop in rip_in_row:
				if rock[1] > stop[1]:
					if stop in final_rock_pos:
						final_rock_pos[stop].append((rock[0], stop[1] + 1 + len(final_rock_pos[stop])))
						break
					else:
						final_rock_pos[stop] = [(rock[0], stop[1] + 1)]
						break
	new_rocks = []
	for k, v in final_rock_pos.items():
		new_rocks.extend(v)
	return new_rocks


